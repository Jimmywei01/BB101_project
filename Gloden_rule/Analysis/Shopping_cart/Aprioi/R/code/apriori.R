library(arules) 
#  ??????DATA ???????????? transactios 
click_detail2 =read.transactions("tag_1232.csv", format ="basket"
                                 ,quote="\n", sep = ",", rm.duplicates=TRUE)
#  ?????? support confidens  ????????????????????????
rule=apriori(click_detail2,parameter=
               list(supp=0.001,conf=0.03,minlen=2,maxlen=10))
#  ?????? lift ??????
a =inspect(head(sort(rule,by="lift"),30))
summary(rule)
write.csv(a, file = "rule_123.csv")
